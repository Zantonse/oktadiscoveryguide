export function IndustrySelector() {
  const { selectedIndustry, setSelectedIndustry, conversationStarted } = useSession()

  const renderOption = (industry) => (
    <div className="industry-option">
      <span className="industry-icon">{industryIcons[industry.icon]}</span>
      <div className="industry-info">
        <span className="industry-name">{industry.name}</span>
        <span className="industry-desc">{industry.description}</span>
      </div>
    </div>
  )

  return (
    <Card title="Industry" className="selector-card">
      <Dropdown
        options={industries}
        value={selectedIndustry}
        onChange={setSelectedIndustry}
        placeholder="Select an industry..."
        groupBy="sector"
        renderOption={renderOption}
        className={conversationStarted ? 'disabled' : ''}
      />
      {selectedIndustry && (
        <div className="selection-badge">
          {industryIcons[selectedIndustry.icon]}
          <span>{selectedIndustry.name}</span>
        </div>
      )}
    </Card>
  )
}
